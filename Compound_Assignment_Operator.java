class Compound_Assignment_Operator
{
    public static void main(String ara[])
    {
         int length = 11;
	     System.out.println(length+=3);
         System.out.println(length-=3);
         System.out.println(length*=3);
         System.out.println(length/=3);
         System.out.println(length%=3);
		
    }
}